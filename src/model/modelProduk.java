package model;

/**
 *
 * @author leocr
 */
public class modelProduk {
    private String kode;
    private String nama;
    private double harga;
    private int stok;
    
    public modelProduk(String kode, String nama, double harga, int stok) {
        this.kode = kode;
        this.nama = nama;
        this.harga = harga;
        this.stok = stok;
    }
    public String getKode() { return kode; }
    public String getNama() { return nama; }
    public double getHarga() { return harga; }
    public int getStok() { return stok; }

}
