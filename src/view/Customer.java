    public boolean checkKode(String kode) {

        return kode.matches("^[a-zA-Z0-9]+$");
    }

    public void tampil_data() {

        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("Kode Customer");
        tabel.addColumn("Nama Customer");
        tabel.addColumn("Alamat Lengkap");
        tabel.addColumn("Provinsi");
        tabel.addColumn("Kota");
        tabel.addColumn("Kecamatan");
        tabel.addColumn("Kelurahan");
        tabel.addColumn("kode Pos");
        try {
            List<modelCustomer> list = controller.ambilSemuaProduk();
            for (modelCustomer p : list) {
                tabel.addRow(new Object[]{
                    p.getKode(),
                    p.getNama(),
                    p.getAlamat(),
                    p.getProvinsi(),
                    p.getKota(),
                    p.getKecamatan(),
                    p.getKelurahan(),
                    p.getKodepos()
                });
            }
            jTable1.setModel(tabel);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

