    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        try {
            if (checkKode(kode_customer.getText())) {
                boolean berhasil = controller.simpan(
                        kode_customer.getText(),
                        nama_customer.getText(),
                        alamat_lengkap.getText(),
                        provinsi.getText(),
                        kota.getText(),
                        kecamatan.getText(),
                        kelurahan.getText(),
                        kode_pos.getText()
                );

                if (berhasil) {
                    clearForm();
                    tampil_data();
                }
            }
            else{
            
             JOptionPane.showMessageDialog(null, "Tidak bisa Spesial Karakter");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal di Simpan");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

